package com.operators;

public class RelationalOperDemo {

    public  static  void main(String [] agrs)
    {
        // relationl operators ->

        // < , > , <= ,>= , == , !=

        int value1 = 10;
        double vlaue2 = 2.2 ; // A-> 65

        System.out.println(vlaue2); //2.2
        System.out.println((int)vlaue2); //2
        System.out.println(value1 < vlaue2);// two values -> true or false  // true // 10 < 2
        System.out.println(value1 > vlaue2); // false
        System.out.println(value1 <= vlaue2);// true
        System.out.println(value1 >= vlaue2);//false
        System.out.println(value1 == vlaue2);//false
        System.out.println(value1 != vlaue2);// true
    }
}
